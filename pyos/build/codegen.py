"""
pyOS Code Generator — honest Python boot ops → C glue
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Dict, List, Tuple

from ..api.errors import UnsupportedOpError
from ..api.interrupts import Interrupts
from ..api.keyboard import Keyboard
from ..api.memory import Memory
from ..api.screen import Screen
from ..api.syscalls import SysCall

if TYPE_CHECKING:
    from ..api.kernel import Kernel


class CodeGenerator:
    def __init__(self, kernel: "Kernel"):
        self.kernel = kernel
        self.arch = kernel.config.arch.value
        self._strings: List[Tuple[str, str]] = []
        self._func_ops: Dict[str, list] = {}
        self._string_counter = 0
        self._has_keypress = bool(kernel._keypress_handlers)
        self._has_timer = bool(kernel._timer_handlers)
        self._timer_ms = (
            min(h.interval_ms for h in kernel._timer_handlers)
            if kernel._timer_handlers
            else 0
        )
        self._symbol_map: List[Dict[str, str]] = []
        self._declared_syms: set = set()

    def generate(self) -> str:
        if self.arch != "x86":
            raise ValueError("Only arch='x86' is supported")

        self._strings = []
        self._func_ops = {}
        self._string_counter = 0
        self._symbol_map = []
        self._declared_syms = set()
        Memory._reset_symbols()

        sorted_funcs = sorted(self.kernel._boot_functions, key=lambda x: x.priority)
        for boot_func in sorted_funcs:
            Screen._reset()
            Keyboard._reset()
            Memory._reset()
            Interrupts._reset()
            SysCall._reset()
            boot_func.func()
            ops = []
            ops.extend(Screen._get_operations())
            ops.extend(Memory._get_operations())
            ops.extend(Keyboard._get_operations())
            ops.extend(Interrupts._get_operations())
            ops.extend(SysCall._get_operations())
            self._func_ops[boot_func.name] = ops
            self._symbol_map.append(
                {"kind": "boot", "name": boot_func.name, "priority": str(boot_func.priority)}
            )
            for op in ops:
                if op.get("type") == "print" and "text" in op:
                    label = f"str_{self._string_counter}"
                    self._string_counter += 1
                    self._strings.append((label, op["text"]))
                    op["_string_label"] = label

        for handler in self.kernel._keypress_handlers:
            self._symbol_map.append({"kind": "keypress", "name": handler.name})
        for handler in self.kernel._timer_handlers:
            self._symbol_map.append(
                {"kind": "timer", "name": handler.name, "ms": str(handler.interval_ms)}
            )

        return self._emit_c(sorted_funcs)

    def generate_asm(self) -> str:
        return "; Use kernel.compile() for C glue.\n"

    def get_symbol_map(self) -> List[Dict[str, str]]:
        return list(self._symbol_map)

    def _c_escape(self, text: str) -> str:
        return (
            text.replace("\\", "\\\\")
            .replace('"', '\\"')
            .replace("\n", "\\n")
            .replace("\r", "\\r")
            .replace("\t", "\\t")
        )

    def _emit_c(self, sorted_funcs: list) -> str:
        cfg = self.kernel.config
        stack_top = cfg.stack_top
        stack_size = cfg.stack_size
        heap_start = cfg.heap_start
        heap_size = cfg.heap_size
        enable_irq = "PYOS_TRUE" if cfg.enable_interrupts else "PYOS_FALSE"
        has_key = "PYOS_TRUE" if self._has_keypress else "PYOS_FALSE"
        has_timer = "PYOS_TRUE" if self._has_timer else "PYOS_FALSE"
        paging = "PYOS_TRUE" if cfg.enable_paging else "PYOS_FALSE"
        user_mode = "PYOS_TRUE" if cfg.enable_user_mode else "PYOS_FALSE"
        processes = "PYOS_TRUE" if cfg.enable_processes else "PYOS_FALSE"
        filesystem = "PYOS_TRUE" if cfg.enable_filesystem else "PYOS_FALSE"
        network = "PYOS_TRUE" if cfg.enable_network else "PYOS_FALSE"
        debug_lab = "PYOS_TRUE" if cfg.debug_level == "lab" else "PYOS_FALSE"
        key_echo = "PYOS_TRUE" if cfg.keypress_mode == "echo" else "PYOS_FALSE"

        lines: List[str] = []
        lines.append("/* Generated by pyOS — do not edit by hand */")
        lines.append("/* Linux i386 syscall ABI (syscall_32.tbl) — enable_linux_abi is always on */")
        if cfg.rootfs_path:
            lines.append(
                f"/* Hint: attach rootfs with: pyos run <image> --disk {self._c_escape(cfg.rootfs_path)} */"
            )
        if cfg.enable_filesystem or cfg.enable_ext2:
            lines.append(
                "/* filesystem/ext2: vfs_mount_root() tries virtio-blk + ext2, else ramfs seeds */"
            )
        if cfg.enable_network:
            lines.append("/* network: net_init() probes virtio-net (use pyos run --net) */")
        if cfg.enable_user_mode and cfg.enable_processes and cfg.enable_filesystem:
            lines.append(
                "/* userland boot: task_start_init() execve's /init from rootfs when disk is attached */"
            )
        lines.append('#include "kernel.h"')
        lines.append('#include "screen.h"')
        lines.append('#include "heap.h"')
        lines.append('#include "debug.h"')
        lines.append('#include "keyboard.h"')
        lines.append('#include "syscall.h"')
        lines.append('#include "pic.h"')
        lines.append('#include "io.h"')
        lines.append('#include "timer.h"')
        lines.append('#include "paging.h"')
        lines.append('#include "gdt.h"')
        lines.append('#include "task.h"')
        lines.append('#include "vfs.h"')
        lines.append('#include "shell.h"')
        lines.append('#include "net.h"')
        lines.append("")
        lines.append("KernelConfig g_kernel_config = {")
        lines.append(f"    .stack_top = 0x{stack_top:X},")
        lines.append(f"    .stack_size = {stack_size}u,")
        lines.append(f"    .heap_start = 0x{heap_start:X},")
        lines.append(f"    .heap_size = {heap_size}u,")
        lines.append(f"    .enable_interrupts = {enable_irq},")
        lines.append(f"    .has_keypress_handler = {has_key},")
        lines.append(f"    .has_timer_handler = {has_timer},")
        lines.append(f"    .timer_interval_ms = {self._timer_ms}u,")
        lines.append(f"    .enable_paging = {paging},")
        lines.append(f"    .enable_user_mode = {user_mode},")
        lines.append(f"    .enable_processes = {processes},")
        lines.append(f"    .enable_filesystem = {filesystem},")
        lines.append(f"    .enable_network = {network},")
        lines.append(f"    .debug_lab = {debug_lab},")
        lines.append(f"    .keypress_echo = {key_echo},")
        lines.append("};")
        lines.append("")

        for label, text in self._strings:
            lines.append(f'static const char {label}[] = "{self._c_escape(text)}";')
        lines.append("")

        # Pre-declare memory symbols used across boot functions
        for boot_func in sorted_funcs:
            for op in self._func_ops.get(boot_func.name, []):
                for key in ("symbol", "out_symbol", "dest", "src"):
                    sym = op.get(key)
                    if sym and isinstance(sym, str) and sym.isidentifier() and sym not in self._declared_syms:
                        if op.get("type") in (
                            "malloc",
                            "calloc",
                            "allocate_static",
                            "realloc",
                            "free",
                            "memset",
                            "memcpy",
                        ) or key in ("dest", "src", "out_symbol"):
                            lines.append(f"static void *{sym} = 0;")
                            self._declared_syms.add(sym)

        lines.append("")

        # Filesystem seed blobs (declared before boot entry)
        seed_decls: List[str] = []
        seed_calls: List[str] = []
        if self.kernel.config.enable_filesystem:
            for path, data in self.kernel._seed_files.items():
                safe = "".join(c if c.isalnum() else "_" for c in path)
                label = f"seed_{safe}"
                try:
                    text = data.decode("ascii")
                    seed_decls.append(f'static const char {label}[] = "{self._c_escape(text)}";')
                    seed_calls.append(
                        f'    vfs_create("{self._c_escape(path)}", (const u8 *){label}, {len(data)}u);'
                    )
                except UnicodeDecodeError:
                    hexes = ", ".join(str(b) for b in data)
                    seed_decls.append(f"static const u8 {label}[] = {{{hexes}}};")
                    seed_calls.append(
                        f'    vfs_create("{self._c_escape(path)}", {label}, {len(data)}u);'
                    )
        for d in seed_decls:
            lines.append(d)
        if seed_decls:
            lines.append("")

        for boot_func in sorted_funcs:
            lines.append(f"/* @kernel.on_boot {boot_func.name} (priority={boot_func.priority}) */")
            lines.append(f"static void boot_{boot_func.name}(void) {{")
            lines.append(f'    debug_log("enter @kernel.on_boot {boot_func.name}");')
            for op in self._func_ops.get(boot_func.name, []):
                lines.extend(self._emit_op(op, boot_func.name))
            lines.append("}")
            lines.append("")

        lines.append("void pyos_user_boot(void) {")
        for boot_func in sorted_funcs:
            lines.append(f"    boot_{boot_func.name}();")
        for c in seed_calls:
            lines.append(c)
        if self.kernel.config.enable_filesystem:
            lines.append("    vfs_mount_root();")
        if self.kernel.config.enable_network:
            lines.append("    net_init();")
        if cfg.enable_processes:
            lines.append("    task_start_init();")
        lines.append("}")
        lines.append("")

        lines.append("void pyos_user_keypress(char ch, u8 scancode) {")
        lines.append("    (void)scancode;")
        if self._has_keypress:
            if cfg.keypress_mode == "echo":
                lines.append("    if (ch) { screen_putchar(ch); }")
            for handler in self.kernel._keypress_handlers:
                lines.append(f'    debug_log("keypress -> {handler.name}");')
            lines.append("    shell_on_key(ch, scancode);")
        else:
            lines.append("    (void)ch;")
        lines.append("}")
        lines.append("")

        lines.append("void pyos_user_timer(void) {")
        if self._has_timer:
            for handler in self.kernel._timer_handlers:
                lines.append(f'    debug_log("timer -> {handler.name}");')
            lines.append("    shell_on_timer();")
        lines.append("}")
        lines.append("")
        return "\n".join(lines)

    def _emit_op(self, op: dict, func_name: str) -> List[str]:
        t = op.get("type")
        out: List[str] = []
        if t == "clear":
            out.append("    screen_clear();")
        elif t == "set_color":
            out.append(f"    screen_set_color({op.get('foreground', 15)}, {op.get('background', 0)});")
        elif t == "set_cursor":
            out.append(f"    screen_set_cursor({op.get('row', 0)}, {op.get('col', 0)});")
        elif t == "print":
            label = op.get("_string_label", "str_0")
            row = op.get("row")
            col = op.get("col")
            fg = op.get("foreground", 15)
            bg = op.get("background", 0)
            color = (bg << 4) | fg
            if row is not None and col is not None:
                out.append(f"    screen_print_at({label}, {row}, {col}, {color});")
            else:
                out.append(f"    screen_set_color({fg}, {bg});")
                out.append(f"    screen_print({label});")
            if op.get("newline", True) and (row is None or col is None):
                out.append("    screen_putchar('\\n');")
        elif t == "print_char":
            ch = self._c_escape(op.get("char", " "))
            fg = op.get("foreground", 15)
            bg = op.get("background", 0)
            color = (bg << 4) | fg
            out.append(
                f"    screen_print_char_at('{ch}', {op.get('row', 0)}, {op.get('col', 0)}, {color});"
            )
        elif t == "scroll_up":
            out.append(f"    screen_scroll_up({op.get('lines', 1)});")
        elif t == "scroll_down":
            out.append(f"    screen_scroll_down({op.get('lines', 1)});")
        elif t == "enable_cursor":
            out.append("    screen_enable_cursor();")
        elif t == "disable_cursor":
            out.append("    screen_disable_cursor();")
        elif t == "malloc":
            sym = op["symbol"]
            out.append(f"    {sym} = heap_malloc({op.get('size', 0)}u);")
        elif t == "allocate_static":
            sym = op["symbol"]
            out.append(f"    {sym} = heap_malloc({op.get('size', 0)}u);")
        elif t == "calloc":
            sym = op["symbol"]
            total = int(op.get("count", 0)) * int(op.get("size", 0))
            out.append(f"    {sym} = heap_calloc(1u, {total}u);")
        elif t == "realloc":
            out.append(
                f"    {op['out_symbol']} = heap_realloc({op['symbol']}, {op.get('new_size', 0)}u);"
            )
        elif t == "free":
            out.append(f"    heap_free({op['symbol']});")
        elif t == "memset":
            out.append(
                f"    heap_memset({op['symbol']}, {op.get('value', 0)}, {op.get('size', 0)}u);"
            )
        elif t == "memcpy":
            out.append(
                f"    heap_memcpy({op['dest']}, {op['src']}, {op.get('size', 0)}u);"
            )
        elif t == "kb_read_line":
            out.append("    (void)keyboard_read_line_echo();")
        elif t == "kb_clear_buffer":
            out.append("    keyboard_clear_buffer();")
        elif t in ("kb_read_key", "kb_read_char", "kb_wait_key"):
            out.append(f"    /* {t}: non-blocking poll available via keyboard_* at runtime */")
        elif t == "irq_enable":
            out.append("    sti();")
        elif t == "irq_disable":
            out.append("    cli();")
        elif t == "irq_mask":
            out.append(f"    pic_mask_irq({op.get('irq', 0)});")
        elif t == "irq_unmask":
            out.append(f"    pic_unmask_irq({op.get('irq', 0)});")
        elif t == "irq_eoi":
            out.append(f"    pic_send_eoi({op.get('irq', 0)});")
        elif t == "syscall_exit":
            out.append(
                f"    (void)syscall_dispatch(SYS_EXIT, {op.get('code', 0)}, 0, 0, 0, 0, 0);"
            )
        elif t == "syscall_sleep":
            out.append(
                f"    (void)syscall_dispatch(SYS_SLEEP, {op.get('ms', 0)}, 0, 0, 0, 0, 0);"
            )
        elif t == "syscall_time":
            out.append("    (void)syscall_dispatch(SYS_TIME, 0, 0, 0, 0, 0, 0);")
        elif t == "syscall_call":
            args = op.get("args") or []
            a1 = args[0] if len(args) > 0 else 0
            a2 = args[1] if len(args) > 1 else 0
            a3 = args[2] if len(args) > 2 else 0
            a4 = args[3] if len(args) > 3 else 0
            a5 = args[4] if len(args) > 4 else 0
            a6 = args[5] if len(args) > 5 else 0
            out.append(
                f"    (void)syscall_dispatch({op.get('syscall', 0)}, "
                f"{a1}, {a2}, {a3}, {a4}, {a5}, {a6});"
            )
        else:
            raise UnsupportedOpError(
                t or "unknown",
                f"Operation recorded in @{func_name} cannot be emitted to C glue",
            )
        return out
